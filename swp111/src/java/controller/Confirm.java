    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User u = (User) session.getAttribute("user");
        String id_raw = request.getParameter("pid");
        int id = Integer.parseInt(id_raw);
        BodyDAO bd = new BodyDAO();
        ProductDAO pd = new ProductDAO();
        Product p = pd.getProductByID(id);
        Wallet w = bd.getWalletById(p.getUser_id());
        double balance = w.getBalance();
        bd.updateCartCompletely(u.getId(),id);
        pd.updateIsConfirm(id);
        bd.updateWallet(balance + p.getActualreceived(), p.getUser_id());
        request.getRequestDispatcher("getallproduct").forward(request, response);
    }

